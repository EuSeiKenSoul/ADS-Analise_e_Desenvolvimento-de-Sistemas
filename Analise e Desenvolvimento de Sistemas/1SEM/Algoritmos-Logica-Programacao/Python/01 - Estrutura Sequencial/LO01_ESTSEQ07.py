print("Este programa calcula o volume de um paralelepípedo.")
print("====================================================")
#declarando variavel e recebendo valores
comp = int(input("Digite um valor para comprimento: "));
larg = int(input("Digite um valor para a largura: "));
altu = int(input("Digite um valor para a altura: "));
#Solucionando problema ou realizando calculos

volu = (comp*larg+altu);


print("====================================================")
print("O volume do paralelepípedo é :",volu);

